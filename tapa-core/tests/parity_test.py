def test_parity_interface_type_counts() -> None:
    """Same iface-type roster per module across pipelines.

    Covers every module that appears in BOTH iface maps. Modules with
    known fixture-driven divergences (slot stream/mmap port naming) are
    documented inline at the comparison point so a future regression
    surfaces here rather than hiding.
    """
    _require_fixture()
    py_project = _build_python_project()
    with tempfile.TemporaryDirectory() as tmp:
        rust_project = _build_rust_project(Path(tmp))

    py_ifaces = _extract_ifaces(py_project)
    rust_ifaces = _extract_ifaces(rust_project)

    # Iface-key set must match.
    assert set(py_ifaces.keys()) == set(rust_ifaces.keys()), (
        f"iface-key set differs:\n  python: {sorted(py_ifaces.keys())}\n"
        f"  rust:   {sorted(rust_ifaces.keys())}"
    )

    mismatches: list[str] = []
    for module in sorted(set(py_ifaces) & set(rust_ifaces)):
        py_types = sorted(i.get("type", "") for i in py_ifaces.get(module, []))
        rust_types = sorted(i.get("type", "") for i in rust_ifaces.get(module, []))
        if py_types == rust_types:
            continue
        mismatches.append(
            f"interface type roster for {module} differs:\n"
            f"  python: {py_types}\n  rust:   {rust_types}"
        )

    assert not mismatches, "iface roster mismatch:\n" + "\n".join(mismatches)


