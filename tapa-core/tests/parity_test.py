def test_parity_parsed_verilog_infrastructure_modules() -> None:
    """Parsed-Verilog parity for every non-grouped module both sides emit.

    For each module that both Python and Rust produce in the lowering pass,
    export the Python Verilog text via the Python exporter and the Rust
    Verilog via Rust's exporter, reparse both with ``tapa_core.rtl.parse``,
    and assert the parsed port-name sets match. Byte-level differences
    (formatting, comments) are tolerated; semantic structure is not.
    """
    _require_fixture()
    from tapa_core import graphir_export, rtl  # type: ignore[import-not-found]

    py_verilog = _export_python_non_grouped_modules()

    with tempfile.TemporaryDirectory() as rust_tmp:
        rust_project = _build_rust_project(Path(rust_tmp))
        # Strip grouped modules so the exporter's DRC pass doesn't flag
        # fixture-side missing wires that Python also fails on. We only
        # compare non-grouped infrastructure modules (fifo, reset_inverter,
        # ctrl_s_axi, FSMs, leaf tasks), not the grouped top/slot RTL.
        infra_only = {
            **rust_project,
            "modules": {
                **rust_project["modules"],
                "module_definitions": [
                    m
                    for m in rust_project["modules"]["module_definitions"]
                    if m.get("module_type") != "grouped_module"
                ],
                "top_name": None,
            },
            "ifaces": None,
        }
        rust_dest = Path(rust_tmp) / "rust_export"
        rust_dest.mkdir()
        graphir_export.export_project(json.dumps(infra_only), str(rust_dest))
        rust_verilog: dict[str, str] = {}
        for v_path in rust_dest.glob("*.v"):
            rust_verilog[v_path.stem] = v_path.read_text(encoding="utf-8")

    # Compare the intersection of modules both pipelines produce. FSM
    # modules are included now that Rust attaches the real Vitis-generated
    # FSM RTL via `build_project_from_paths` instead of synthesizing a
    # stub.
    common = set(py_verilog) & set(rust_verilog)
    # At minimum these must be present and comparable on both sides.
    required = {
        "fifo",
        "reset_inverter",
        "VecAdd_control_s_axi",
        "VecAdd_fsm",
        "SLOT_X0Y2_SLOT_X0Y2_fsm",
        "SLOT_X2Y3_SLOT_X2Y3_fsm",
        "SLOT_X3Y3_SLOT_X3Y3_fsm",
    }
    assert required.issubset(common), (
        f"infrastructure modules missing from one side:\n"
        f"  python: {sorted(set(py_verilog))}\n"
        f"  rust:   {sorted(set(rust_verilog))}\n"
        f"  common: {sorted(common)}"
    )

    mismatches: list[str] = []
    for module in sorted(common):
        try:
            py_parsed = rtl.parse(py_verilog[module])
        except Exception as exc:  # noqa: BLE001
            mismatches.append(f"{module}: python export unparsable: {exc}")
            continue
        try:
            rust_parsed = rtl.parse(rust_verilog[module])
        except Exception as exc:  # noqa: BLE001
            mismatches.append(f"{module}: rust export unparsable: {exc}")
            continue
        py_ports = {p["name"] for p in py_parsed.get("ports") or []}
        rust_ports = {p["name"] for p in rust_parsed.get("ports") or []}
        if py_ports != rust_ports:
            mismatches.append(
                f"{module}: port-name mismatch\n"
                f"  python-only: {sorted(py_ports - rust_ports)}\n"
                f"  rust-only:   {sorted(rust_ports - py_ports)}"
            )

    assert not mismatches, "parsed Verilog differs on:\n" + "\n".join(mismatches)


