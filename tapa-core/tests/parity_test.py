def _build_python_abgraph() -> dict[str, Any]:
    """Run Python's ABGraph generator on the shared VecAdd program + fp config.

    The shared fixture doesn't populate synthesis area per task, so we
    stamp a placeholder zero-area onto every task before running. Rust's
    `collect_task_area` does the same thing internally (defaults to zero
    when `annotations.total_area` is absent), so both sides start from
    the same zero baseline for area comparison.
    """
    sys.path.insert(0, str(_REPO_ROOT))
    pa_test = importlib.import_module("tapa.graphir_conversion.project_assembly_test")
    gen_abgraph = importlib.import_module("tapa.abgraph.gen_abgraph")

    program = pa_test._build_program()  # type: ignore[attr-defined]  # noqa: SLF001
    zero_area = {"LUT": 0, "FF": 0, "BRAM_18K": 0, "DSP": 0, "URAM": 0}
    for task in program._tasks.values():  # noqa: SLF001
        # The property setter refuses to overwrite; assign the backing
        # field directly so both leaf and upper tasks get a stamp.
        task._self_area = dict(zero_area)  # noqa: SLF001
        task._total_area = dict(zero_area)  # noqa: SLF001

    fp_config = _REPO_ROOT / "tests" / "functional" / "abgraph" / "test_fp_config.json"
    graph = gen_abgraph.get_top_level_ab_graph(program, fp_config)
    return graph.model_dump(mode="json")


def _build_rust_abgraph() -> dict[str, Any]:
    """Run Rust's ABGraph generator (RTL-aware) on the same fixture."""
    from tapa_core import floorplan  # type: ignore[import-not-found]

    with open(_FIXTURE_DIR / "graph.json", encoding="utf-8") as f:
        graph = json.load(f)
    graph["target"] = _TARGET
    graph["slot_task_name_to_fp_region"] = _SLOT_TO_REGION
    design_json = json.dumps(graph)
    modules_json = json.dumps(_rtl_sources())

    # Use the RTL-aware entrypoint so FIFO widths and port widths are
    # derived from the actual Verilog (matching what Python does via the
    # program's attached modules).
    result = floorplan.get_top_level_ab_graph_from_rtl(
        design_json, modules_json, "{}", "VecAdd_fsm"
    )
    return json.loads(result)


