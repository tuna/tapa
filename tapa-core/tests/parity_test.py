def test_parity_upper_module_parameters() -> None:
    """Upper grouped modules carry their Vitis-declared parameters.

    Python's `ports_builder` additionally **aggregates** parameters from the
    child-leaf RTL (e.g., `ap_ST_fsm_state*`) into slot modules. Rust only
    lifts the slot task's own declared RTL parameters into the grouped
    module. The aggregation logic is a deeper tapa-rtl/tapa-lowering
    integration outside this round's scope.

    This test verifies:
      * Every non-empty Python parameter list has a matching Rust entry
        on the top module (VecAdd).
      * Slot modules carry *at least* their own RTL-declared parameters
        (the aggregation delta is tolerated with a documented inline
        comment).
    """
    _require_fixture()
    py_project = _build_python_project()
    with tempfile.TemporaryDirectory() as tmp:
        rust_project = _build_rust_project(Path(tmp))

    def param_names(project: dict[str, Any], name: str) -> set[str]:
        for m in project["modules"]["module_definitions"]:
            if m["name"] == name:
                return {p["name"] for p in m.get("parameters") or []}
        return set()

    # Top task VecAdd: full parameter parity is expected (both sides derive
