"""Cross-language parity tests: Python reference vs Rust `tapa_core`.

These tests drive both the Python TAPA pipeline and the Rust reimplementation
on a single shared fixture, then compare normalized outputs structurally.

The fixture is ``tapa/graphir_conversion/top_conversion_test_files`` — the
same one used by ``project_assembly_test.py`` in the Python tree. It provides
a VecAdd program with slot-task RTL, an FSM, a control_s_axi module, and
cross-references against ``tests/functional/graphir`` for the device config
and floorplan.

Run locally with ``python3 -m pytest tapa-core/tests/parity_test.py``.
"""

